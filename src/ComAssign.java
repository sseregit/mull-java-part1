public class ComAssign {
    public static void main(String[] args) {
        int result =0, data = 10;
        result += 3;
        System.out.printf("%d\n", result);
        result *= data;
        System.out.printf("%d\n", result);
        result /= 2;
        System.out.printf("%d\n", result);
        result -= data - 5;
        System.out.printf("%d\n", result);
        result %= 3;
        System.out.printf("%d\n", result);
    }
}
